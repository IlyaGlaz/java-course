package com.iglaz.javacourse.lesson1;

public class Literal {
    public static void main(String[] args) {
        System.out.println(25); // int
        System.out.println(25L); // long

        System.out.println(0.25); // double
        System.out.println(0.25F); // float

        System.out.println('A'); // char

        System.out.println(true); // boolean

        System.out.println("Hello World!"); // Literal but not a primitive
    }
}
